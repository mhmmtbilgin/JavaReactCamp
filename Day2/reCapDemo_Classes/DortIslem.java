package reCapDemo_Classes;

public class DortIslem {

	public int topla(int sayi1, int sayi2) {
		System.out.println("Toplamanin Sonucu: " + (sayi1+sayi2));
		return sayi1 + sayi2;
	}

	public int cikar(int sayi1, int sayi2) {
		System.out.println("Cikarmanin Sonucu : " + (sayi1-sayi2));
		return sayi1 - sayi2;
	}

	public int carp(int sayi1, int sayi2) {
		System.out.println("Carpmanin Sonucu : " + (sayi1*sayi2));
		return sayi1 * sayi2;
	}

	public int bol(int sayi1, int sayi2) {
		System.out.println("Bolmenin Sonucu : " + (sayi1/sayi2));
		return sayi1 / sayi2;
	}
}


