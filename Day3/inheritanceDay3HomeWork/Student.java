package inheritanceDay3HomeWork;

public class Student extends User {

	private String studentNumber;
	private String studentEducationDegree;
	
	public String getStudentNumber() {
		return studentNumber;
	}
	public void setStudentNumber(String studentNumber) {
		this.studentNumber = studentNumber;
	}
	public String getStudentEducationDegree() {
		return studentEducationDegree;
	}
	public void setStudentEducationDegree(String studentEducationDegree) {
		this.studentEducationDegree = studentEducationDegree;
	}

}


